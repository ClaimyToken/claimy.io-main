/**
 * Build-time: writes `src/environments/environment.prod.ts` from process.env.
 * Angular bakes `environment` at compile time — Netlify env vars must be applied *before* `ng build`.
 *
 * **Never** inject private RPC URLs (e.g. Shyft with api_key) — anything in this file ships in `main.*.js`.
 * The browser always uses public mainnet RPC for optional SPL reads; Shyft belongs in Supabase Edge secrets
 * (`SOLANA_RPC_URL` on `claimy-credits` / `withdraw-spl` only).
 *
 * Runs when:
 *   - `NETLIFY=true` (Netlify sets this during builds), or
 *   - `CLAIMY_SPL_MINT` / `CLAIMY_SUPABASE_URL` / `CLAIMY_SUPABASE_ANON_KEY` is set, or
 *   - `CLAIMY_FORCE_ENV_INJECT=1`
 *
 * Otherwise exits without changing the file (local `npm run build` keeps committed defaults).
 */
const fs = require('fs');
const path = require('path');

/** Public mainnet only — do not replace with a keyed provider URL. */
const PUBLIC_MAINNET_RPC = 'https://api.mainnet-beta.solana.com';

function readLaunchMintFallback() {
  try {
    const launchPath = path.join(__dirname, '..', 'src', 'app', 'config', 'claimy-launch.config.ts');
    const src = fs.readFileSync(launchPath, 'utf8');
    const m = src.match(/claimySplMintAddress:\s*['"]([^'"]+)['"]/);
    return m ? m[1] : 'CVNAP5GpSBVQfqtqfu4jVRMVYmTKBqS5a5mDMPFUpump';
  } catch {
    return 'CVNAP5GpSBVQfqtqfu4jVRMVYmTKBqS5a5mDMPFUpump';
  }
}

const defaults = {
  supabaseUrl: 'https://mosmjagamrtsyeoohcty.supabase.co',
  supabaseAnonKey:
    'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZSIsInJlZiI6Im1vc21qYWdhbXJ0c3llb29oY3R5Iiwicm9sZSI6ImFub24iLCJpYXQiOjE3NzM5NzYxOTQsImV4cCI6MjA4OTU1MjE5NH0.1NUfVb3kOMhqJfeE_DxG0YFu7g9TFs7K4ZnuQqBMaWo',
  solanaRpcUrl: PUBLIC_MAINNET_RPC,
  claimySplMintAddress: readLaunchMintFallback()
};

function trimEnv(name) {
  const v = process.env[name];
  if (v == null || !String(v).trim()) return null;
  return String(v).trim();
}

const shouldInject =
  process.env.NETLIFY === 'true' ||
  process.env.CLAIMY_FORCE_ENV_INJECT === '1' ||
  !!trimEnv('CLAIMY_SPL_MINT') ||
  !!trimEnv('CLAIMY_SUPABASE_URL') ||
  !!trimEnv('CLAIMY_SUPABASE_ANON_KEY');

if (!shouldInject) {
  console.log('[inject-env-prod] Skip — using committed environment.prod.ts (set NETLIFY=true or CLAIMY_* / CLAIMY_FORCE_ENV_INJECT=1 to inject).');
  process.exit(0);
}

const supabaseUrl = trimEnv('CLAIMY_SUPABASE_URL') || defaults.supabaseUrl;
const supabaseAnonKey = trimEnv('CLAIMY_SUPABASE_ANON_KEY') || defaults.supabaseAnonKey;
const solanaRpcUrl = defaults.solanaRpcUrl;
const claimySplMintAddress = trimEnv('CLAIMY_SPL_MINT') || defaults.claimySplMintAddress;

const content = `/**
 * Generated by scripts/inject-env-prod.cjs at build time. Do not edit by hand — it is overwritten on Netlify/CI.
 * Browser \`solanaRpcUrl\` is always public mainnet (no API keys). Private RPC → Supabase Edge \`SOLANA_RPC_URL\`.
 */
export const environment = {
  production: true,
  supabaseUrl: ${JSON.stringify(supabaseUrl)},
  supabaseAnonKey: ${JSON.stringify(supabaseAnonKey)},
  solanaRpcUrl: ${JSON.stringify(solanaRpcUrl)},
  claimySplMintAddress: ${JSON.stringify(claimySplMintAddress)}
};
`;

const out = path.join(__dirname, '..', 'src', 'environments', 'environment.prod.ts');
fs.writeFileSync(out, content, 'utf8');
console.log('[inject-env-prod] Wrote', path.relative(process.cwd(), out));
console.log('[inject-env-prod] solanaRpcUrl: public mainnet only (private RPC stays in Supabase Edge secrets).');
